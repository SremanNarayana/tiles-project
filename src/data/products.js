/* ─── Helper ──────────────────────────────────────────────────────────── */
export const getCategoryBySlug = (slug) =>
  CATEGORIES.find((c) => c.slug === slug) || null;

export const getProductsByCategory = (slug) =>
  PRODUCTS.filter((p) => p.category === slug);

export const searchProducts = (slug, query, subcategory, sortBy) => {
  let results = getProductsByCategory(slug);

  if (subcategory && subcategory !== 'All') {
    results = results.filter((p) => p.subcategory === subcategory);
  }

  if (query.trim()) {
    const q = query.toLowerCase();
    results = results.filter(
      (p) =>
        p.name.toLowerCase().includes(q) ||
        p.description.toLowerCase().includes(q) ||
        p.brand.toLowerCase().includes(q) ||
        p.tags.some((t) => t.toLowerCase().includes(q))
    );
  }

  if (sortBy === 'name-az')   results = [...results].sort((a, b) => a.name.localeCompare(b.name));
  if (sortBy === 'name-za')   results = [...results].sort((a, b) => b.name.localeCompare(a.name));
  if (sortBy === 'popular')   results = [...results].sort((a, b) => (b.isPopular ? 1 : 0) - (a.isPopular ? 1 : 0));
  if (sortBy === 'instock')   results = [...results].sort((a, b) => (b.inStock ? 1 : 0) - (a.inStock ? 1 : 0));

  return results;
};

